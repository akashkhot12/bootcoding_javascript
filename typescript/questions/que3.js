// print odd elements from array given number using user input.
function printOdd(arr) {
    for (var index = 0; index < arr.length; index++) {
        if (arr[index] === 81) {
            console.log(arr[index] + " " + "this odd number!");
        }
    }
}
var a2 = printOdd([10, 21, 31, 4, 5, 6, 7, 81, 9, 10]);
